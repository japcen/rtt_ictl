// ETH
void phy_reset(void)
{
    //return;
    rt_pin_mode(RT_ETH_RST_Pin, PIN_MODE_OUTPUT);
    rt_pin_write(RT_ETH_RST_Pin, PIN_HIGH);
    rt_thread_mdelay(50);
    rt_pin_write(RT_ETH_RST_Pin, PIN_LOW);
    rt_thread_mdelay(50);
    rt_pin_write(RT_ETH_RST_Pin, PIN_HIGH);
}
